
// Entity Class
public class Fruit {

    private Long id;
    
    private String fruit;
    
    private double price;
    
    // Getter ...
    public Long getId() {
        return id;
    }

    // SETTER ...
    public void setId(Long id) {
        this.id = id;
    }

    // Getter ...
    public String getFruit() {
        return fruit;
    }

    // SETTER ...
    public void setFruit(String fruit) {
        this.fruit = fruit;
    }

    // Getter ...
    public double getPrice() {
        return price;
    }

    // SETTER ...
    public void setPrice(double price) {
        this.price = price;
    }  
}
